length = float(input("Длина: "))
width = float(input("Ширина: "))
height = float(input("Высота: "))

# Вычисляем площадь и объем комнаты
area = length * width
volume = length * width * height

# Выводим результат
print("Площадь комнаты:", area)
print("Объем комнаты:", volume)

 


